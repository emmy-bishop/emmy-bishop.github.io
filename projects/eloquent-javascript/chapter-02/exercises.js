function triangles(rows) {
  var count = '';
  for (var i = 0; i < rows; i++) {
    count += '#';
    console.log(count);
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var i = 1; i <= 15; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log('fizzbuzz');
    } else if (i % 3 === 0) {
      console.log('fizz');
    } else if (i % 5 === 0) {
      console.log('buzz');
    } else {
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(rows) {
  var evenLines = 0;
  var oddLines = 1;
      for (var j = 0; j < rows; j++) {
        if (evenLines % 2 !== 0) {
          evenLines++;
        } else {
          oddLines++;
        }
      if ((oddLines + evenLines) % 2 !== 0) {
        console.log('# # # # \n');
      } else if ((oddLines + evenLines) % 2 === 0) {
        console.log(' # # # # \n');
  }
 }
}


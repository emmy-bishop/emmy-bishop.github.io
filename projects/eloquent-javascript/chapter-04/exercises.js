////////////////////////////////////////////////////////////////////////////////

// create array to populate with numbers
// set base case to start === end
// call function again with args of start + step, end, step

var numbers = [];

function range(start, end, step = 1) {
  if (start === end) {
    return numbers;
  }
  numbers.push(start);
  return range(start + step, end, step);
}

